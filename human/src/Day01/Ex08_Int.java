package Day01;

public class Ex08_Int {
public static void main(String[] args) {
	/*
	 * 리터럴 :프로그램에서 직접 표현한 값 
	 * 정수 - 10,8(0),16(0x)진수
	*/
	int var1 = 10;
	System.out.println("var1:"+var1);
	int var2 = 023;
	System.out.println("var2:"+var2);
	int var3 = 0x42;
	System.out.println("var3:"+var3);
	
}
}
